    private void txtcariKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtcariKeyReleased
        // TODO add your handling code here:
        try 
        {
            
            sdf=new SimpleDateFormat("dd-MM-yyyy");
            model = (DefaultTableModel)tblmhs.getModel();
            model.getDataVector().removeAllElements();
            model.fireTableDataChanged(); 
                
            java.sql.Connection conn=(Connection)koneksi.configDB();
            //cari data berdasarkan kategori dari ComboBox
            String sql="select * from mahasiswa where "+cbkategori.getSelectedItem()+" like ?";
            PreparedStatement pst=conn.prepareStatement(sql);
            pst.setString(1, "%"+txtcari.getText()+"%");
            ResultSet rs=pst.executeQuery();
            
            while(rs.next())
            {
                model.addRow(new Object[]{
                    rs.getString("Nim"),
                    rs.getString("nama"),
                    rs.getString("notelp"),
                    rs.getString("jurusan"),
                    rs.getString("alamat"),
                    rs.getString("alamat"),
                    sdf.format(rs.getDate("tgl_daftar"))
                        
                });
                
                
            }
              
            
           tblmhs.setModel(model);
                           
              
        } 
        
        catch (Exception e) 
        {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_txtcariKeyReleased

