    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(validasi())
        {
            JOptionPane.showMessageDialog(null, "Silahkan Pilih Mahasiswa Terlebih Dahulu");
        }
        
        else
        {
            try 
            {
                File reportFile=new File("src/report/KartuPerpus.jrxml");
                JasperDesign jDesign=JRXmlLoader.load(reportFile);
                Map formValues=new HashMap();
                formValues.put("nim", txtnim.getText().toString().trim());
                formValues.put("nama", txtnama.getText().toString().trim());
                
                JasperReport jr=JasperCompileManager.compileReport(jDesign);
                JasperPrint jp = JasperFillManager.fillReport(jr, formValues,new JREmptyDataSource());
                JasperViewer.viewReport(jp,false);
                    
            } 
            
            catch (JRException ex) 
            {
                Logger.getLogger(datamhs.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }//GEN-LAST:event_jButton3ActionPerformed

